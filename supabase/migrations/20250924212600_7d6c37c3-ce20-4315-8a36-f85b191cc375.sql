-- Fix the trigger functions that might not have search_path set
CREATE OR REPLACE FUNCTION public.update_portfolio_updated_at()
RETURNS trigger
LANGUAGE plpgsql
SET search_path = 'public'
AS $function$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$function$;

